
#ifndef Platform_Types
#define Platform_Types

typedef signed char                                 int8_t;
typedef unsigned char                               uint8_t;
typedef short                                       int16_t;
typedef unsigned short                              uint16_t;
typedef int                                         int32_t;
typedef unsigned                                    uint32_t;
typedef long long                                   int64_t;
typedef unsigned long long                          uint64_t;
typedef float                                       float32_t;
typedef double                                      double64_t;
typedef long double                                 double80_t;

typedef volatile signed char                        vint8_t;
typedef volatile unsigned char                      vuint8_t;
typedef volatile short                              vint16_t;
typedef volatile unsigned short                     vuint16_t;
typedef volatile int                                vint32_t;
typedef volatile unsigned                           vuint32_t;
typedef volatile long long                          vint64_t;
typedef volatile unsigned long long                 vuint64_t;
typedef volatile float                              vfloat32_t;
typedef volatile double                             vdouble64_t;
typedef volatile long double                        vdouble80_t;

#endif
